    <h2 class="h2">
        Smink személyesen rád szabva!
    </h2>
    <p>
        Sminkeimet a természeteség jegyeiben
        készítem, hogy a tükörben magadat lásd vissza.
        Személyre szabva alkotom meg a sminked,
        arcvonásaid kiemelésével és közös
        megbeszéléssel.
        A folyamat közben szeretek mindent alaposan
        átbeszélni, hiszen az a lényeg, hogy távozáskor a
        lehető legszebbnek érezd magad.
    </p>
    <p>
        Ha a Nagy Napon engem választasz
        sminkesednek, úgy javaslom, hogy előtte
        készítsünk egy próbasminket, amely folyamán
        minden részletet van időnk alaposan átbeszélni,
        megismerjük egymást.
    </p>
