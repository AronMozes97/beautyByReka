<div class="page-text">
    <h4 class="text-center">Arctisztító kezelés</h4>
    <p>
        Az egészséges és ápolt bőrhöz elengedhetetlen a
        rendszeres, kozmetikus által végzett arctisztítás.
        Problémás, zsíros bőr esetén 1-2 havonta, míg
        normál vagy száraz bőr esetén negyed évente
        javaslom az arctisztítását.
    <p>
        <b class="cosmetic-bold-text">A kezelés menete:</b> a bőr arctejjel való
        letisztítása, enzimes peeling, a bőr felpuhítása gőz
        alatt, közben arcmasszázs, tisztítás, ferőtlenítés,
        ultrahang(opcionális - lásd nagykezelés),
        bőrnyugtató pakolás, befejezőkrém.
    </p>
    <p>
        A kezelés várható időtartalma: 90-120 perc
    </p>
